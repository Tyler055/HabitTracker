from flask import Flask, jsonify, render_template
from flask_migrate import Migrate
from flask_jwt_extended import JWTManager
from flask_cors import CORS
from dotenv import load_dotenv
import os
from config import config
from app.utils.extensions import db, ma, mail, mongo  # Centralized extension objects
from app.routes.habit_routes import habit_bp
from app.routes.auth_routes import auth_bp

# Load environment variables
load_dotenv()

# Initialize extensions
jwt = JWTManager()
migrate = Migrate()

def create_app(env_name=None):
    app = Flask(__name__)
    CORS(app, resources={r"/api/*": {"origins": "http://localhost:3000"}})

    # Load config by environment
    env = env_name or os.getenv('FLASK_ENV', 'development')
    app.config.from_object(config[env])

    # Override DB URL from environment if provided
    app.config['SQLALCHEMY_DATABASE_URI'] = os.getenv(
        "DATABASE_URL", app.config.get("SQLALCHEMY_DATABASE_URI")
    )
    
    # Ensure the DATABASE_URL is set
    if not app.config['SQLALCHEMY_DATABASE_URI']:
        raise ValueError("DATABASE_URL is not set in the environment variables.")
    
    app.config['SQLALCHEMY_TRACK_MODIFICATIONS'] = False
    app.config['JWT_SECRET_KEY'] = os.getenv("JWT_SECRET_KEY", "super-secret-key")

    # Initialize extensions only once
    db.init_app(app)
    ma.init_app(app)
    migrate.init_app(app, db)
    jwt.init_app(app)
    mail.init_app(app)
    mongo.init_app(app)

    # MongoDB setup
    app.config["MONGO_URI"] = os.getenv("MONGO_PROD_URI")
    mongo.init_app(app)

    # Register blueprints
    app.register_blueprint(habit_bp, url_prefix="/api/habits")
    app.register_blueprint(auth_bp, url_prefix="/api/auth")

    # Register global error handlers
    register_error_handlers(app)

    # Serve the main HTML file for React to handle routing
    @app.route('/')
    def index():
        return render_template('index.html')  # This serves the React app
    

    # Example API endpoint to fetch data
    @app.route('/api/data')
    def get_data():
        data = {
            "message": "Hello from Flask!"
        }
        return jsonify(data)

    # Example of dynamic HTML content
    @app.route('/api/get_html')
    def get_html():
        # Example dynamic HTML content generated by Flask
        dynamic_html = """
        <div>
            <h2>Dynamic Content from Flask</h2>
            <p>This content is generated by the back end (Flask) and displayed in the front end (React).</p>
        </div>
        """
        return jsonify({"html": dynamic_html})

    # Test MongoDB route
    @app.route("/test-mongo")
    def test_mongo():
        try:
            # Try to access a collection in your MongoDB
            mongo.db.users.find_one()
            return "MongoDB is connected and the route works!"
        except Exception as e:
            return f"Error connecting to MongoDB: {str(e)}"
        
    @app.route("/test")
    def test():
        return render_template("test.html")

    return app

# Global error handlers
def register_error_handlers(app):
    @app.errorhandler(404)
    def not_found(error):
        return {"message": "Resource not found"}, 404

    @app.errorhandler(500)
    def internal_error(error):
        return {"message": "Internal server error"}, 500


if __name__ == '__main__':
    app = create_app()
    app.run(debug=True)
